export const filterExercises = (exercises, filters) => {
    return exercises.filter(exercise => {

        if (filters.equipment && filters.equipment !== 'All') {
            if (exercise.equipment !== filters.equipment) return false;
        }

        if (filters.difficulty && filters.difficulty !== 'All') {
            if (exercise.difficulty !== filters.difficulty) return false;
        }

        if (filters.duration && filters.duration !== 'All') {
            const durationNum = parseInt(exercise.duration);
            if (filters.duration === '5 min' && durationNum !== 5) return false;
            if (filters.duration === '10 min' && durationNum !== 10) return false;
            if (filters.duration === '15 min' && durationNum !== 15) return false;
            if (filters.duration === '20 min' && durationNum !== 20) return false;
        }
        return true;
    });
};